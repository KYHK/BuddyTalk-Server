package user;

public class UserDTO {
	
	String userId;
	String userPassword;
	String userName;
	String userDepartment_num;
	String userDepartment;
	String userPhone_num;
	String userEmail;
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getUserPassword() {
		return userPassword;
	}
	public void setUserPassword(String userPassword) {
		this.userPassword = userPassword;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getUserDepartment_num() {
		return userDepartment_num;
	}
	public void setUserDepartment_num(String userDepartment_num) {
		this.userDepartment_num = userDepartment_num;
	}
	public String getUserDepartment() {
		return userDepartment;
	}
	public void setUserDepartment(String userDepartment) {
		this.userDepartment = userDepartment;
	}
	public String getUserPhone_num() {
		return userPhone_num;
	}
	public void setUserPhone_num(String userPhone_num) {
		this.userPhone_num = userPhone_num;
	}
	public String getUserEmail() {
		return userEmail;
	}
	public void setUserEmail(String userEmail) {
		this.userEmail = userEmail;
	}

	
	

}
